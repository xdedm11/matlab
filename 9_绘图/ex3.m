x1=0:pi/10:pi;
y1=sin(x1);
x2=-2*pi:pi/10:0;
y2=10*cos(x2);
plot(x1,y1,'k')
h1=gca;
h2=axes('Position',get(h1,'Position'));
figure(2)
plot(x2,y2,'r')
set(h2,'Xaxislocation','top')
set(h2,'Yaxislocation','right')
set(h2,'Color','none')
legend(h1,'y1=sin(x)')
legend(h2,'y2=10cosx')
h1.Xcolor='r'
h1.YColor='r'
h2.XColor='b'
h2.YColor='b'